"""Representacao de numeros em notacao cientifica."""

tempo = 4.8e-6  # s
distancia = 3.84e8  # m

print(f"Tempo na forma decimal: {tempo}")
print(f"Tempo em notacao cientifica: {tempo:.2e} s")
print(f"Distancia em notacao cientifica: {distancia:.2e} m")

produto = (3e4) * (2e-2)
quociente = (8e7) / (2e3)

print(f"Produto: {produto:.2e}")
print(f"Quociente: {quociente:.2e}")

